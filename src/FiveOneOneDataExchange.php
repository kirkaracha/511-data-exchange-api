<?php declare(strict_types=1);

namespace Kirkaracha\FiveOneOneDataDataExchange;

class FiveOneOneDataExchange
{
    /**
     * Multiplies the two given numbers
     * @param int $a
     * @param int $b
     * @return int
     */
    public function multiply($a, $b): int
    {
        return $a * $b;
    }
}
